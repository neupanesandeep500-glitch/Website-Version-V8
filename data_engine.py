def norm_dist(s):
    if not s:
        return ""
    s = str(s).lower()
    s = re.sub(r"[()]", " ", s)
    s = s.replace("_", " ").replace("-", " ")
    s = re.sub(r"[^a-z\s]", "", s)
    s = re.sub(r"\s+", " ", s).strip()
    return s

def _find_district_substring(norm_text):
    """Scan a normalised free-form string (e.g. a full postal address like
    'Pokhara-5, Kaski' or 'Ward No. 3, Damak, Jhapa') for any known district
    name/alias appearing as a whole word/phrase, and return its canonical key."""
    padded = f" {norm_text} "
    for tok in _DISTRICT_TOKENS_SORTED:
        if f" {tok} " in padded:
            return _DISTRICT_TOKEN_TO_CANON[tok]
    return None

def district_to_province(district):
    """Resolve a raw district/VDC/address cell to a province name. Tries, in order:
    (1) exact province-name alias, (2) canonical district / district alias,
    (3) a close-spelling fuzzy match (typos / stray words like 'Dist.'),
    (4) a whole-word scan for a known district name embedded anywhere inside
        a longer string — this is what lets a full address such as
        'Ward 5, Pokhara, Kaski' or 'Damak-3, Jhapa' still resolve correctly
        even though the cell isn't JUST a district name."""
    if not district:
        return None
    key = norm_dist(district)
    if not key:
        return None
    if key in PROVINCE_NAME_ALIASES:
        return PROVINCE_NAME_ALIASES[key]
    resolved = DISTRICT_ALIASES.get(key, key)
    prov = DISTRICT_PROVINCE.get(resolved)
    if prov:
        return prov
    prov = PROVINCE_NAME_ALIASES.get(resolved)
    if prov:
        return prov
    # Fuzzy fallback — catches minor typos / stray words not yet in the alias
    # list, without guessing wildly (cutoff keeps false positives rare).
    match = difflib.get_close_matches(key, _ALL_DISTRICT_KEYS, n=1, cutoff=0.82)
    if match:
        resolved2 = DISTRICT_ALIASES.get(match[0], match[0])
        prov = DISTRICT_PROVINCE.get(resolved2)
        if prov:
            return prov
    # Substring fallback — the cell may be a full address/VDC/ward string
    # rather than a bare district name.
    found = _find_district_substring(key)
    if found:
        return DISTRICT_PROVINCE.get(found)
    return None

