    @staticmethod
    def _sample_bbox(lat_min, lat_max, lon_min, lon_max, max_grid=5):
        lat_min, lat_max = min(lat_min, lat_max), max(lat_min, lat_max)
        lon_min, lon_max = min(lon_min, lon_max), max(lon_min, lon_max)
        ny = max(1, min(max_grid, int((lat_max - lat_min) / 0.012) + 1))
        nx = max(1, min(max_grid, int((lon_max - lon_min) / 0.012) + 1))
        out = []
        for iy in range(ny):
            la = lat_min if ny == 1 else lat_min + (lat_max - lat_min) * (iy + 0.5) / ny
            for ix in range(nx):
                lo = lon_min if nx == 1 else lon_min + (lon_max - lon_min) * (ix + 0.5) / nx
                out.append((la, lo))
        return out

    @staticmethod
    def _sample_polyline(coords, step_deg=0.01):
        pts = [(la, lo) for la, lo in coords if la is not None and lo is not None]
        if not pts:
            return []
        out = [pts[0]]
        for (la1, lo1), (la2, lo2) in zip(pts, pts[1:]):
            n = max(1, int(max(abs(la2 - la1), abs(lo2 - lo1)) / step_deg))
            for k in range(1, n + 1):
                out.append((la1 + (la2 - la1) * k / n, lo1 + (lo2 - lo1) * k / n))
        return out

    def pa_check(self, bbox=None, coords=None):
        """Tests a feature's geometry against every protected-area polygon.
        Returns (share_of_geometry_inside_any_PA, 'PA1 (cat) / PA2 (cat)')."""
        if not self.pa_loaded:
            return 0.0, ""
        if bbox:
            pkey = "pa:" + ",".join(f"{v:.5f}" for v in bbox)
        else:
            pts0 = [(la, lo) for la, lo in (coords or []) if la is not None]
            pkey = ("pa:" + ";".join(f"{la:.5f},{lo:.5f}" for la, lo in pts0[:4])
                    + f"|{len(pts0)}") if pts0 else None
        if pkey:
            cached = self._cache_get(pkey)
            if cached is not None:
                return cached["s"], cached["l"]
        samples = (self._sample_bbox(*bbox) if bbox
                   else self._sample_polyline(coords or []))
        if not samples:
            return 0.0, ""
        votes = defaultdict(int)
        hit = 0
        for la, lo in samples:
            inside_any = False
            for p in self.pa:
                b = p["bbox"]
                if b[0] <= lo <= b[2] and b[1] <= la <= b[3]:
                    if self._inside(p, lo, la):
                        votes[(p["name"], p["category"])] += 1
                        inside_any = True
            if inside_any:
                hit += 1
        if not votes:
            if pkey:
                self._cache_put(pkey, {"s": 0.0, "l": ""})
            return 0.0, ""
        ordered = sorted(votes, key=votes.get, reverse=True)
        label = " / ".join(f"{n} ({c})" if c else n for n, c in ordered)
        share = hit / len(samples)
        if pkey:
            self._cache_put(pkey, {"s": share, "l": label})
        return share, label

    def canonical_district_name(self, raw_text):
        """Map a raw district text cell to the official GIS display name
        (e.g. 'kavre' -> 'Kabhrepalanchok'), or None if unresolvable."""
        if not raw_text:
            return None
        key = norm_dist(raw_text)
        canon = DISTRICT_ALIASES.get(key, key)
        for name in self.district_province:
            nk = norm_dist(name)
            if nk == canon or DISTRICT_ALIASES.get(nk, nk) == canon:
                return name
        return None


