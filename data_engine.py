def record_local(r):
    """Local-government (palika) label for a record, resolved lazily from
    the GIS local layer using the licence-area centre (or the point) and
    cached on the record dict once found. A miss is NOT cached — if GIS
    finishes loading (or is re-synced) after this was first called, the
    next call gets a fresh chance instead of being stuck empty forever."""
    if r.get("_local"):
        return r["_local"]
    out = None
    if GIS.loaded and GIS.locals:
        if r.get('bbox'):
            la = (r['bbox'][0] + r['bbox'][1]) / 2
            lo = (r['bbox'][2] + r['bbox'][3]) / 2
            out = GIS.locate_local(la, lo)
        elif r.get('coords'):
            la, lo = r['coords'][0]
            out = GIS.locate_local(la, lo)
    if out:
        r["_local"] = out
    return out

