def _norm_sheet(sheet_name):
    """Normalise a sheet name for classification: lower-case, collapse
    underscores/whitespace to single spaces, strip. Sheet-naming in
    different DoED/Google-Sheets exports varies a lot ('Survey_Hydro_Less 1'
    vs 'Survey Hydro' vs 'Application for Survey ...'), so classification
    below matches on stems/substrings rather than brittle exact prefixes."""
    return re.sub(r"[_\s]+", " ", str(sheet_name).strip().lower())

def classify_status(sheet_name):
    """Classify a sheet into a licensing-pipeline stage, OR one of the
    "side" categories (Cancelled / GoN Study Project / Technical Clearance)
    that sit outside the pipeline funnel. Checked in this order so a
    cancellation/GoN/technical-clearance sheet is never mis-read as an
    ordinary pipeline stage just because it also mentions "survey" etc."""
    n = _norm_sheet(sheet_name)

    if "cancel" in n:
        return "Cancelled"
    if n.startswith("gon"):
        return "GoN Study Project"
    if "technical clearance" in n:
        return "Technical Clearance"

    is_appl = n.startswith("appl")  # "Appl_..." / "Application for ..."
    if is_appl and "survey" in n:
        return "Application for Survey License"
    if is_appl and "construct" in n:
        return "Application for Construction License"
    if n.startswith("survey"):
        return "Survey License"
    if "construct" in n:
        return "Construction License"
    if n.startswith("power plant"):
        return "Operating"
    return "Unknown"

def classify_substage(sheet_name):
    """Finer detail behind the 3 "side" categories — which stage a
    cancellation applied to, or whether a GoN project is studied / still
    under study. Returns None for ordinary pipeline-stage sheets."""
    n = _norm_sheet(sheet_name)

    if "cancel" in n:
        if n.startswith("appl") and "survey" in n:
            return "Survey Application — Cancelled"
        if "survey" in n and "license" in n:
            return "Survey License — Cancelled"
        if "40" in n and "criteria" in n:
            return "40 MW Criteria License — Cancelled"
        if "generation" in n and "appl" in n:
            return "Generation Application — Cancelled"
        if "generation" in n and ("lic" in n):
            return "Generation License — Cancelled"
        return "Cancelled — Other"

    if n.startswith("gon"):
        if "under study" in n:
            return "Under Study"
        if "studied" in n:
            return "Studied"
        return "GoN Study"

    if "technical clearance" in n:
        return "Technical Clearance"

    return None

def classify_type_from_name(sheet_name):
    n = _norm_sheet(sheet_name)
    if "transmi" in n: return "Transmission Line"
    if "solar" in n: return "Solar"
    if "wind" in n: return "Wind"
    if "co generation" in n or "cogeneration" in n: return "Co-generation"
    if "biomass" in n: return "Biomass"
    if "thermal" in n: return "Thermal"
    if "hydro" in n:
        if "more" in n: return "Hydro (>1MW)"
        if "less" in n: return "Hydro (<=1MW)"
        return None   # ambiguous size — let per-row capacity decide
    return None

