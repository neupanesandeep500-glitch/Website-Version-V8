def _luminance(hex_color):
    hex_color = hex_color.lstrip("#")
    if len(hex_color) != 6:
        return 0.5
    r, g, b = (int(hex_color[i:i+2], 16) / 255.0 for i in (0, 2, 4))
    def lin(c): return c/12.92 if c <= 0.03928 else ((c+0.055)/1.055) ** 2.4
    return 0.2126*lin(r) + 0.7152*lin(g) + 0.0722*lin(b)

def readable_text(hex_color):
    """White text on dark fills, dark text on light fills — keeps pie/bar
    labels legible regardless of the underlying colour."""
    return "#ffffff" if _luminance(hex_color) < 0.52 else "#1a1a1a"

def wrap_label(s, width=11):
    return "\n".join(textwrap.wrap(str(s), width)) if s else s

def draw_donut(ax, labels, values, colors, title=None, min_pct_for_wedge_text=6):
    """Donut chart with a clean side LEGEND instead of text labels stuck to
    each wedge. Wedge-hugging labels collide badly once there are several
    thin slices (e.g. Wind / Co-generation / Thermal / Other all under 5%);
    a legend scales to any number of categories without ever overlapping."""
    pairs = [(l, v) for l, v in zip(labels, values) if v and v > 0]
    if not pairs:
        ax.text(0.5, 0.5, "No data", ha="center", va="center", transform=ax.transAxes,
                 fontsize=9, color=MC["muted"])
        if title: ax.set_title(title, fontweight="bold", fontsize=10)
        return
    lbls = [p[0] for p in pairs]
    vals = [p[1] for p in pairs]
    cols = [colors[labels.index(l)] if l in labels else "#999" for l in lbls]
    total = sum(vals)

    donut = SETTINGS.get("pie_style", "Donut") != "Pie"
    wprops = {"edgecolor": "white", "linewidth": 1.3}
    if donut:
        wprops["width"] = 0.42
    wedges, _, autotexts = ax.pie(
        vals, colors=cols, startangle=90,
        autopct=lambda p: f"{p:.0f}%" if p >= min_pct_for_wedge_text else "",
        pctdistance=0.78 if donut else 0.7,
        wedgeprops=wprops)
    for at, col in zip(autotexts, cols):
        at.set_color(readable_text(col)); at.set_fontweight("bold"); at.set_fontsize(7.5)

    legend_labels = [f"{wrap_label(l, 18)} — {v/total*100:.0f}%" for l, v in zip(lbls, vals)]
    outside_legend(ax, wedges, legend_labels, fontsize=7.4,
                   labelspacing=0.55, handlelength=1.1, handleheight=1.1)
    if title:
        ax.set_title(title, fontweight="bold", fontsize=10)

