    # ── geometry ───────────────────────────────────────────────────────────
    @staticmethod
    def _point_in_rings(x, y, rings):
        """Even-odd ray casting across every ring (outer rings + holes)."""
        inside = False
        for ring in rings:
            n = len(ring)
            j = n - 1
            for i in range(n):
                xi, yi = ring[i]
                xj, yj = ring[j]
                if (yi > y) != (yj > y):
                    xin = (xj - xi) * (y - yi) / (yj - yi) + xi
                    if x < xin:
                        inside = not inside
                j = i
        return inside

    def locate(self, lat, lon):
        """(district_name, province_name) containing the WGS-84 point, or
        the NEAREST district if the point sits marginally outside every
        polygon (common for river / border coordinates). (None, None) if the
        point is nowhere near Nepal."""
        if lat is None or lon is None:
            return None, None
        x, y = lon, lat
        cands = [d for d in self.districts
                 if d["bbox"][0] <= x <= d["bbox"][2] and d["bbox"][1] <= y <= d["bbox"][3]]
        for d in cands:
            if self._inside(d, x, y):
                return d["name"], d["province"]
        # near-miss fallback: closest ring vertex within ~3 km (0.03 deg)
        return self.locate_nearest(lat, lon, max_deg=0.03)

    def locate_nearest(self, lat, lon, max_deg=0.03):
        """Snap a point to the nearest district boundary within max_deg
        (used for river/border coordinates and last-resort rescue)."""
        if lat is None or lon is None:
            return None, None
        x, y = lon, lat
        best, bestd = None, max_deg ** 2
        m = max_deg + 0.05
        near = [d for d in self.districts
                if d["bbox"][0] - m <= x <= d["bbox"][2] + m
                and d["bbox"][1] - m <= y <= d["bbox"][3] + m]
        for d in near:
            for ring in d["rings"]:
                for px, py in ring[::4]:
                    dd = (px - x) ** 2 + (py - y) ** 2
                    if dd < bestd:
                        bestd, best = dd, d
        if best:
            return best["name"], best["province"]
        return None, None

    def _vote_result(self, samples):
        """Shared vote counting for polyline / bbox sampling. Returns a dict:
        district, province, share, districts_all, provinces_all, n_prov."""
        votes = defaultdict(int)
        prov_of = {}
        for la, lo in samples:
            d, p = self.locate(la, lo)
            if d:
                votes[d] += 1
                prov_of[d] = p
        if not votes:
            # last-resort rescue: snap the feature's midpoint to the nearest
            # district within ~15 km so no coordinate-bearing row stays
            # 'Unspecified'
            mid = samples[len(samples) // 2]
            d, p = self.locate_nearest(mid[0], mid[1], max_deg=0.15)
            if not d:
                return dict(district=None, province=None, share=0.0,
                            districts_all="", provinces_all="", n_prov=0)
            votes[d] = 1; prov_of[d] = p
        win = max(votes, key=votes.get)
        provs = sorted({prov_of[d] for d in votes})
        return dict(district=win, province=prov_of[win],
                    share=votes[win] / max(1, sum(votes.values())),
                    districts_all=" / ".join(sorted(votes, key=votes.get, reverse=True)),
                    provinces_all=" / ".join(provs), n_prov=len(provs))

    def locate_majority(self, coords, step_deg=0.01):
        """MAJORITY-PORTION rule for point / polyline features. `coords` is a
        list of (lat, lon) tuples (1 point for a plant, 2+ for a line). The
        polyline is densified every ~1 km and each sample votes for the
        district it falls inside; the district holding the majority of the
        feature wins. Returns the _vote_result dict."""
        pts = [(la, lo) for la, lo in coords if la is not None and lo is not None]
        if not pts:
            return dict(district=None, province=None, share=0.0,
                        districts_all="", provinces_all="", n_prov=0)
        key = ("m:" + ";".join(f"{la:.5f},{lo:.5f}" for la, lo in pts[:4])
               + f"|{len(pts)}")
        cached = self._cache_get(key)
        if cached is not None:
            return cached
        samples = [pts[0]]
        for (la1, lo1), (la2, lo2) in zip(pts, pts[1:]):
            seg = max(abs(la2 - la1), abs(lo2 - lo1))
            n = max(1, int(seg / step_deg))
            for k in range(1, n + 1):
                samples.append((la1 + (la2 - la1) * k / n,
                                lo1 + (lo2 - lo1) * k / n))
        res = self._vote_result(samples)
        self._cache_put(key, res)
        return res

    def locate_bbox(self, lat_min, lat_max, lon_min, lon_max, max_grid=5):
        """LARGEST-OVERLAP-AREA rule for the DoED licence-area bounding box
        (Latitude min/max × Longitude min/max). The box is sampled on a
        regular grid and each cell votes for the district containing it, so
        the winner is the district/province holding the largest share of the
        licensed area — the same assignment the R sf::st_intersection /
        slice_max(overlap_area) workflow produces. Degenerate boxes collapse
        to a point. Returns the _vote_result dict."""
        if lat_min is None or lon_min is None:
            return dict(district=None, province=None, share=0.0,
                        districts_all="", provinces_all="", n_prov=0)
        if lat_max is None: lat_max = lat_min
        if lon_max is None: lon_max = lon_min
        lat_min, lat_max = min(lat_min, lat_max), max(lat_min, lat_max)
        lon_min, lon_max = min(lon_min, lon_max), max(lon_min, lon_max)
        key = f"b:{lat_min:.5f},{lat_max:.5f},{lon_min:.5f},{lon_max:.5f}"
        cached = self._cache_get(key)
        if cached is not None:
            return cached
        ny = max(1, min(max_grid, int((lat_max - lat_min) / 0.012) + 1))
        nx = max(1, min(max_grid, int((lon_max - lon_min) / 0.012) + 1))
        samples = []
        for iy in range(ny):
            la = lat_min if ny == 1 else lat_min + (lat_max - lat_min) * (iy + 0.5) / ny
            for ix in range(nx):
                lo = lon_min if nx == 1 else lon_min + (lon_max - lon_min) * (ix + 0.5) / nx
                samples.append((la, lo))
        res = self._vote_result(samples)
        self._cache_put(key, res)
        return res

