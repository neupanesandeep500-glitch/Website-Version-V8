    def load_protected(self, extra_dirs=None):
        """Load the protected-area polygon layer (Protected_Area.zip or loose
        shapefiles whose name contains 'protect'/'संरक्षित'). Optional — the
        dashboard works without it, the PA map view simply explains."""
        if _pyshp is None:
            self.pa_error = "python module 'pyshp' not installed"
            return False
        try:
            rd = self._find_pa_reader(extra_dirs=extra_dirs)
            if rd is None:
                self.pa_error = ("protected-area package not found next to the "
                                 "application or workbook (Protected_Area.zip)")
                return False
            sf, src = rd
            fields = [f[0] for f in sf.fields[1:]]
            self.pa = []
            for sh, rec in zip(sf.shapes(), sf.records()):
                d = dict(zip(fields, list(rec)))
                name = str(d.get("PAS_NAME") or d.get("Name") or "Protected area").strip()
                cat = str(d.get("Category") or "").strip()
                x0, y0, x1, y1 = sh.bbox
                self.pa.append(dict(name=name, category=cat,
                                    rings=self._rings_of(sh),
                                    bbox=(x0, y0, x1, y1)))
            self.pa_source = src
            self.pa_loaded = True
            return True
        except Exception as exc:
            self.pa_error = f"failed to read protected-area package: {exc}"
            return False

