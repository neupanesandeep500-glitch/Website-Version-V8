def outside_legend(ax, handles=None, labels=None, *, where="right",
                   ncol=None, fontsize=7.5, title=None, **kw):
    """A legend that is GUARANTEED to sit outside the plot area. With
    constrained_layout figures, matplotlib reserves room for it, so it can
    never overlap bars/lines/maps."""
    args = ()
    if handles is not None:
        args = (handles,) if labels is None else (handles, labels)
    if where == "bottom":
        h = handles if handles is not None else ax.get_legend_handles_labels()[0]
        ncol = ncol or min(max(len(h), 1), 3)
        leg = ax.legend(*args, loc="upper center", bbox_to_anchor=(0.5, -0.16),
                        ncol=ncol, fontsize=fontsize, frameon=False,
                        title=title, columnspacing=1.1, handletextpad=0.5, **kw)
    else:
        leg = ax.legend(*args, loc="center left", bbox_to_anchor=(1.02, 0.5),
                        ncol=ncol or 1, fontsize=fontsize, frameon=False,
                        title=title, borderaxespad=0.2, **kw)
    try:
        leg.set_in_layout(True)
    except Exception:
        pass
    return leg

def draw_categorical(ax, labels, values, colors, *, ylabel=None, title=None,
                     value_fmt="{:,.0f}", style=None, label_size=7.5):
    """One categorical comparison, rendered in the chart style chosen on the
    Settings tab (vertical bar / horizontal bar / line / area)."""
    style = style or SETTINGS.get("chart_style", "Bar (vertical)")
    show_vals = SETTINGS.get("show_values", True)
    n = len(labels)
    if n == 0 or not any(values):
        ax.text(0.5, 0.5, "No data", ha="center", va="center",
                transform=ax.transAxes, fontsize=9, color=MC["muted"])
        if title: ax.set_title(title)
        return
    accent = colors[0] if colors else MC["accent"]
    if style == "Bar (horizontal)":
        bars = ax.barh(labels, values, color=colors, edgecolor="white", linewidth=0.6)
        ax.invert_yaxis()
        ax.set_xlim(0, max(values) * 1.22)
        ax.grid(axis="x"); ax.grid(axis="y", visible=False)
        if ylabel: ax.set_xlabel(ylabel)
        if show_vals:
            for b, v in zip(bars, values):
                ax.annotate(value_fmt.format(v), (b.get_width(), b.get_y()+b.get_height()/2),
                            ha="left", va="center", fontsize=label_size,
                            color=MC["text"], xytext=(3, 0), textcoords="offset points")
    elif style in ("Line", "Area"):
        x = list(range(n))
        ax.plot(x, values, color=accent, marker="o", markersize=5,
                linewidth=2.2, zorder=3)
        if style == "Area":
            ax.fill_between(x, 0, values, color=accent, alpha=0.18, zorder=2)
        ax.set_xticks(x)
        ax.set_xticklabels(labels, fontsize=label_size,
                           rotation=30 if max(len(str(l)) for l in labels) > 8 else 0,
                           ha="right" if max(len(str(l)) for l in labels) > 8 else "center")
        ax.set_ylim(0, max(values) * 1.22)
        if ylabel: ax.set_ylabel(ylabel)
        if show_vals:
            for xi, v in zip(x, values):
                ax.annotate(value_fmt.format(v), (xi, v), ha="center", va="bottom",
                            fontsize=label_size, color=MC["text"],
                            xytext=(0, 4), textcoords="offset points")
    else:  # Bar (vertical)
        bars = ax.bar(labels, values, color=colors, edgecolor="white",
                      linewidth=0.6, width=0.62)
        ax.set_ylim(0, max(values) * 1.22)
        ax.tick_params(axis="x", labelsize=label_size,
                       rotation=25 if max(len(str(l)) for l in labels) > 9 else 0)
        if ylabel: ax.set_ylabel(ylabel)
        if show_vals:
            for b, v in zip(bars, values):
                ax.annotate(value_fmt.format(v), (b.get_x()+b.get_width()/2, b.get_height()),
                            ha="center", va="bottom", fontsize=label_size, color=MC["text"])
    if title:
        ax.set_title(title)

def thin_year_ticks(ax, labels, max_ticks=16, fontsize=7):
    """Categorical year axes become an unreadable overlap when the B.S. year
    span is long — show at most max_ticks evenly spaced labels (always
    including the first and last year), rotated 45°, so tick labels can
    never collide regardless of how many years the data covers."""
    n = len(labels)
    if n == 0:
        return
    step = max(1, math.ceil(n / max_ticks))
    idx = list(range(0, n, step))
    if idx[-1] != n - 1 and (n - 1 - idx[-1]) >= max(1, step // 2):
        idx.append(n - 1)
    elif idx[-1] != n - 1:
        idx[-1] = n - 1
    ax.set_xticks(idx)
    ax.set_xticklabels([str(labels[i]) for i in idx], rotation=45,
                       ha="right", fontsize=fontsize)

