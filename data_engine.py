def norm_header(h):
    if h is None: return ""
    return re.sub(r"\s+", " ", str(h)).strip().lower()

def find_col(headers, *keywords):
    for i, h in enumerate(headers):
        hn = norm_header(h)
        if all(k in hn for k in keywords):
            return i
    return None

def parse_bs(value):
    """Return (y,m,d) tuple treating the raw digits as a BS date, or None."""
    if value is None: return None
    if isinstance(value, datetime.datetime):
        y, m, d = value.year, value.month, value.day
    elif isinstance(value, str):
        s = value.strip()
        if not s or s == "0000-00-00": return None
        m_ = re.match(r"(\d{4})-(\d{1,2})-(\d{1,2})", s)
        if not m_: return None
        y, m, d = int(m_.group(1)), int(m_.group(2)), int(m_.group(3))
    else:
        return None
    if y < 1900 or y > 2100 or m == 0 or d == 0 or m > 12 or d > 32:
        return None
    return (y, m, d)

def bs_str(t):
    if not t: return "—"
    y, m, d = t
    return f"{y:04d}-{m:02d}-{d:02d} BS"

_BS_INPUT_RE = re.compile(r"^\s*(\d{4})(?:[-/. ]+(\d{1,2}))?(?:[-/. ]+(\d{1,2}))?\s*$")

def parse_bs_input(s, end=False):
    """Parse a user-typed B.S. date filter: 'YYYY', 'YYYY-MM' or 'YYYY-MM-DD'
    (also / . or space separators). Missing parts default to the start of the
    period, or its end when end=True, so ranges stay inclusive.
    Returns a (y, m, d) tuple or None."""
    if not s or not str(s).strip():
        return None
    m = _BS_INPUT_RE.match(str(s))
    if not m:
        return None
    y = int(m.group(1))
    mo = int(m.group(2)) if m.group(2) else (12 if end else 1)
    dy = int(m.group(3)) if m.group(3) else (32 if end else 1)
    mo = min(max(mo, 1), 12)
    return (y, mo, dy)

def today_bs():
    """Approximate today's Bikram Sambat date. BS runs ~56 years 8.5 months
    ahead of AD (Baisakh 1 falls around 13-14 April); ±a few days of error is
    irrelevant at the month granularity used by the expiry watchlist."""
    t = datetime.date.today()
    y, m = t.year + 56, t.month + 8
    if t.day >= 14:
        m += 1
    while m > 12:
        m -= 12; y += 1
    return (y, m, 15)

_BS_MONTH_LEN_APPROX = [31, 32, 31, 32, 31, 30, 30, 30, 29, 29, 29, 30]

def today_bs_display():
    """Approximate today's B.S. date WITH a day-of-month, for the header
    clock. Uses 13-Apr as the Baisakh-1 reference and average month
    lengths — good to within a day or two, which is fine for a live
    display (not used anywhere for date-range filtering/business logic;
    see today_bs() above for that)."""
    t = datetime.date.today()
    ny_this_year = datetime.date(t.year, 4, 13)
    if t >= ny_this_year:
        epoch, bs_year = ny_this_year, t.year + 57
    else:
        epoch, bs_year = datetime.date(t.year - 1, 4, 13), t.year + 56
    remaining = (t - epoch).days
    m = 0
    while m < 11 and remaining >= _BS_MONTH_LEN_APPROX[m]:
        remaining -= _BS_MONTH_LEN_APPROX[m]
        m += 1
    return bs_year, m + 1, remaining + 1

def bs_display_str():
    y, m, d = today_bs_display()
    return f"{d} {BS_MONTHS[m-1]} {y} B.S."

def bs_ordinal(t):
    """Sortable integer; approximate (BS months ~29-32 days) but preserves order."""
    if not t: return -1
    y, m, d = t
    return y * 400 + m * 32 + d

