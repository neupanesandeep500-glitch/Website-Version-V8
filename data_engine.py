class DataLoader:
    def __init__(self, path):
        self.path = path
        self.records = []
        self.error = None
        self.sheet_report = []
        self.loc_report = []      # per-sheet coordinate-column diagnostics
        self.unmapped_districts = defaultdict(int)   # raw text -> row count, when no province could be resolved

    def load(self):
        try:
            self._load_workbook()
            self.error = None
        except Exception as exc:
            self.error = str(exc)
            import traceback; traceback.print_exc()

    def _load_workbook(self):
        wb = openpyxl.load_workbook(self.path, data_only=True, read_only=True)
        raw = []
        self.sheet_report = []
        self.loc_report = []
        self.unmapped_districts = defaultdict(int)

        for sn in wb.sheetnames:
            ws = wb[sn]
            status = classify_status(sn)
            type_hint = classify_type_from_name(sn)
            substage = classify_substage(sn)

            rows = list(ws.iter_rows(values_only=True))
            if not rows:
                # Sheet exists (DoED/Sheets already reserved the name for this
                # category) but carries zero rows — most likely no licenses/
                # applications/cancellations exist yet for this stage. Record
                # it so it's visible in diagnostics rather than vanishing
                # silently; the moment the source is updated with real rows,
                # the very same load path below picks it up automatically —
                # no code change needed here.
                self.sheet_report.append((sn, status, type_hint or "(auto)", 0,
                                          "empty sheet (0 rows) — not yet published"))
                continue
            # The header row is not always row 1 — DoED exports often carry a
            # merged title row (and sometimes a blank row) above the headers.
            header_idx = 0
            for hi in range(min(5, len(rows))):
                if find_col(rows[hi], "project") is not None and find_col(rows[hi], "capacity") is not None:
                    header_idx = hi
                    break
            headers = rows[header_idx]
            is_transmission = find_col(headers, "voltage") is not None
            has_river = find_col(headers, "river") is not None

            c_proj  = find_col(headers, "project")
            c_cap   = find_col(headers, "capacity")
            c_volt  = find_col(headers, "voltage") if is_transmission else None
            c_len   = find_col(headers, "line length") if is_transmission else None
            c_river = find_col(headers, "river") if has_river else None
            c_licno = find_col(headers, "lic no")
            if c_licno is None:
                c_licno = find_col(headers, "appn no")
            c_date  = find_col(headers, "isuue date")
            if c_date is None: c_date = find_col(headers, "issue date")
            if c_date is None: c_date = find_col(headers, "appn date")
            # Cancellation sheets record the CANCELLATION decision date —
            # captured here so the Cancelled/GoN timeline chart has years.
            if c_date is None: c_date = find_col(headers, "decision date")
            c_valid    = find_col(headers, "validity")
            c_cod      = find_col(headers, "c o d")
            c_promoter = find_col(headers, "promoter")
            c_district = find_col(headers, "vdc")
            if c_district is None: c_district = find_col(headers, "district")
            # Address / office-location columns often carry the district name
            # embedded in a fuller string (e.g. "Ward 5, Pokhara, Kaski") —
            # used as a fallback whenever the dedicated district column is
            # missing or its value doesn't resolve to a province.
            addr_col_idxs = [i for i, h in enumerate(headers)
                             if any(k in norm_header(h) for k in ("address", "location"))]

            # GIS coordinate columns, in priority order:
            #   1. split Deg/Min/Sec triplets  ("Latitude (Deg)" ...)
            #   2. plain Lat/Long decimal or DMS-text pairs (incl. From/To)
            #   3. UTM Easting/Northing pairs (auto zone 44/45)
            #   4. free-form coordinate / GPS text cells
            dms_lat = find_dms_triplet(headers, "lat")
            dms_lon = find_dms_triplet(headers, "lon")
            use_dms = dms_lat is not None and dms_lon is not None
            lat_cols, lon_cols = ([], []) if use_dms else find_latlon_cols(headers)
            latlon_pairs = list(zip(lat_cols, lon_cols))
            # DoED plant tables give the LICENCE AREA as a coordinate range
            # (Latitude min/max × Longitude min/max) -> treat as a bounding
            # box and assign by LARGEST OVERLAP AREA. From/To column pairs
            # (transmission alignments) stay polyline features instead.
            _fromto = any(re.search(r"\b(from|to)\b", norm_header(headers[i]) or "")
                          for i in lat_cols + lon_cols)
            bbox_mode = (len(lat_cols) >= 2 and len(lon_cols) >= 2
                         and not _fromto and not is_transmission)
            utm_pairs = find_utm_col_pairs(headers)
            coord_col_idxs = [i for i, h in enumerate(headers)
                              if any(k in norm_header(h) for k in ("coordinate", "gps"))]

            def _hdr(i): return str(headers[i]).strip() if i is not None and headers[i] is not None else "?"
            detected_cols = []
            if use_dms:
                detected_cols.append("DMS: " + "/".join(_hdr(i) for i in dms_lat if i is not None)
                                     + " + " + "/".join(_hdr(i) for i in dms_lon if i is not None))
            if bbox_mode:
                detected_cols.append("BBOX(area-majority): "
                                     + "/".join(_hdr(i) for i in lat_cols[:2])
                                     + " × " + "/".join(_hdr(i) for i in lon_cols[:2]))
            else:
                detected_cols += [f"Lat/Lon: {_hdr(a)} + {_hdr(b)}" for a, b in latlon_pairs]
            detected_cols += [f"UTM: {_hdr(a)} + {_hdr(b)}" for a, b in utm_pairs]
            detected_cols += [f"Text: {_hdr(i)}" for i in coord_col_idxs]
            sheet_loc = dict(sheet=sn, coord_cols=detected_cols,
                             district_col=(_hdr(c_district) if c_district is not None else None),
                             n=0, n_gis=0, n_text=0, n_un=0)
            self.loc_report.append(sheet_loc)

            if c_proj is None or c_cap is None:
                # A sheet with only its placeholder/title row (no real data
                # rows underneath) reads as "not yet published" rather than
                # a broken column layout — this is the common case for a
                # licensing stage that currently has zero entries (e.g. no
                # cancellations recorded yet for a category). Once DoED/the
                # sheet owner adds real header + data rows, header_idx above
                # will find them (it scans the first 5 rows on every load)
                # and this sheet starts flowing through normally with no
                # further code changes required.
                note = ("no data rows yet — not yet published" if len(rows) <= 1
                       else "skipped (no project/capacity col found — check headers manually)")
                self.sheet_report.append((sn, status, type_hint or "(auto)", 0, note))
                continue

            n_rows = 0
            for row in rows[header_idx + 1:]:
                if c_proj >= len(row): continue
                proj = row[c_proj]
                if proj is None or str(proj).strip() == "": continue
                cap = to_float(row[c_cap]) if c_cap < len(row) else None

                rtype = type_hint
                if rtype is None:
                    if is_transmission:
                        rtype = "Transmission Line"
                    elif has_river:
                        rtype = "Hydro (>1MW)" if (cap or 0) > 1 else "Hydro (<=1MW)"
                    else:
                        pname = str(proj).lower()
                        if "solar" in pname: rtype = "Solar"
                        elif "wind" in pname: rtype = "Wind"
                        elif "cogen" in pname or "co-gen" in pname: rtype = "Co-generation"
                        else: rtype = "Other"

                date_bs = parse_bs(row[c_date]) if c_date is not None and c_date < len(row) else None
                cod_bs  = parse_bs(row[c_cod])  if c_cod  is not None and c_cod  < len(row) else None
                # Prefer COD for the "Operating" status as the primary sortable date
                primary_date = cod_bs if (status == "Operating" and cod_bs) else date_bs

                # ── GIS location: gather every coordinate on the row ─────
                # NOTE ON DATUMS: DoED's own "Latitude"/"Longitude" sheet
                # columns (plain pairs, DMS triplets, and the licence-area
                # bounding box) are recorded on the Survey Department's
                # Everest 1830 datum, not WGS-84 — even though the column
                # headers don't say so. The GIS boundary layer (district/
                # province polygons, protected areas) is digitised in
                # WGS-84. Converting here, once, at ingestion means every
                # downstream consumer (point-in-polygon district lookup,
                # the map, the data table) works in one consistent system.
                # UTM Easting/Northing is left alone: utm_pair_to_latlon()
                # is already a WGS-84 projection formula, not a datum that
                # needs shifting.
                coords, bbox = [], None
                if use_dms:
                    la, lo = valid_nepal_pair(combine_dms(row, dms_lat), combine_dms(row, dms_lon))
                    if la is not None:
                        coords.append(ct.everest_to_wgs84(la, lo))
                if bbox_mode:
                    lats, lons = [], []
                    for i in lat_cols:
                        v = parse_coord(row[i]) if i < len(row) else None
                        if v is not None:
                            if NEPAL_LAT[0] <= v <= NEPAL_LAT[1]: lats.append(v)
                            elif NEPAL_LON[0] <= v <= NEPAL_LON[1]: lons.append(v)
                    for i in lon_cols:
                        v = parse_coord(row[i]) if i < len(row) else None
                        if v is not None:
                            if NEPAL_LON[0] <= v <= NEPAL_LON[1]: lons.append(v)
                            elif NEPAL_LAT[0] <= v <= NEPAL_LAT[1]: lats.append(v)
                    if lats and lons:
                        # Bounding box corners converted individually (not
                        # just the midpoint) so the box keeps its shape
                        # under the shift instead of just sliding as a point.
                        lo_lat, lo_lon = ct.everest_to_wgs84(min(lats), min(lons))
                        hi_lat, hi_lon = ct.everest_to_wgs84(max(lats), max(lons))
                        bbox = (lo_lat, hi_lat, lo_lon, hi_lon)
                        coords.append(((bbox[0] + bbox[1]) / 2, (bbox[2] + bbox[3]) / 2))
                else:
                    for li, gi in latlon_pairs:
                        la = parse_coord(row[li]) if li < len(row) else None
                        lo = parse_coord(row[gi]) if gi < len(row) else None
                        la, lo = valid_nepal_pair(la, lo)
                        if la is not None:
                            coords.append(ct.everest_to_wgs84(la, lo))
                for ei, ni in utm_pairs:
                    e = to_float(row[ei]) if ei < len(row) else None
                    n_ = to_float(row[ni]) if ni < len(row) else None
                    la, lo = utm_pair_to_latlon(e, n_)
                    if la is not None:
                        coords.append((la, lo))
                for ci in coord_col_idxs:
                    if ci < len(row) and row[ci]:
                        coords.extend(coords_from_text(row[ci]))

                gis_district = gis_province = None
                gis_share = 0.0
                districts_all = provinces_all = ""
                n_prov = 0
                pa_share, pa_names_s = 0.0, ""
                if GIS.pa_loaded and (bbox or coords):
                    pa_share, pa_names_s = GIS.pa_check(bbox=bbox, coords=coords or None)
                if GIS.loaded and (bbox or coords):
                    if bbox:
                        # Largest-overlap-area rule over the licence-area box
                        res = GIS.locate_bbox(*bbox)
                    else:
                        # Majority-portion rule along the point/polyline
                        res = GIS.locate_majority(coords)
                    gis_district, gis_province = res["district"], res["province"]
                    gis_share = res["share"]
                    districts_all, provinces_all = res["districts_all"], res["provinces_all"]
                    n_prov = res["n_prov"]

                district_val = (str(row[c_district]).strip()
                                if c_district is not None and c_district < len(row) and row[c_district] else None)
                province_val = district_to_province(district_val) if district_val else None
                loc_source = "District column" if province_val else None

                # Normalise text districts to the official GIS spelling so the
                # district filter shows ONE entry per real district.
                if district_val and GIS.loaded:
                    canon = GIS.canonical_district_name(district_val)
                    if canon:
                        district_val = canon

                # GIS coordinates are authoritative — they override the text.
                if gis_district:
                    district_val = gis_district
                    province_val = gis_province
                    loc_source = "GIS coordinates"

                # Fallback: scan any address/location column for a district
                # name embedded in the fuller text — this is what makes
                # rows resolve even when the district column is blank or
                # only the address carries the location.
                if not province_val:
                    for idx in addr_col_idxs:
                        if idx < len(row) and row[idx]:
                            addr_txt = str(row[idx]).strip()
                            p2 = district_to_province(addr_txt)
                            if p2:
                                province_val = p2
                                loc_source = "Address text"
                                if not district_val:
                                    district_val = addr_txt
                                break

                if district_val and not province_val:
                    self.unmapped_districts[district_val] += 1

                rec = dict(
                    type=rtype, status=status, substage=substage, sheet=sn,
                    project=str(proj).strip(),
                    capacity_mw=cap,
                    voltage_kv=to_float(row[c_volt]) if c_volt is not None and c_volt < len(row) else None,
                    line_length_km=to_float(row[c_len]) if c_len is not None and c_len < len(row) else None,
                    river=(str(row[c_river]).strip() if c_river is not None and c_river < len(row) and row[c_river] else None),
                    lic_or_app_no=(str(row[c_licno]).strip() if c_licno is not None and c_licno < len(row) and row[c_licno] not in (None, "") else None),
                    date_bs=date_bs,
                    validity_bs=parse_bs(row[c_valid]) if c_valid is not None and c_valid < len(row) else None,
                    cod_bs=cod_bs,
                    primary_date=primary_date,
                    promoter=(str(row[c_promoter]).strip() if c_promoter is not None and c_promoter < len(row) and row[c_promoter] else None),
                    district=district_val,
                    province=province_val or "Unspecified",
                    coords=coords,                       # [(lat, lon), ...]
                    bbox=bbox,                           # licence area (latmin,latmax,lonmin,lonmax)
                    lat=coords[0][0] if coords else None,
                    lon=coords[0][1] if coords else None,
                    gis_share=gis_share,                 # majority vote share
                    districts_all=districts_all,         # every district touched
                    provinces_all=provinces_all,         # every province touched
                    cross_boundary=("Yes" if n_prov > 1 else ("No" if n_prov == 1 else "")),
                    in_pa=("Yes" if pa_share > 0 else ("No" if (bbox or coords) else "")),
                    pa_share=pa_share,
                    pa_names=pa_names_s,
                    loc_source=loc_source or "—",
                )
                raw.append(rec)
                n_rows += 1
                sheet_loc["n"] += 1
                if rec['loc_source'] == "GIS coordinates":
                    sheet_loc["n_gis"] += 1
                elif rec['loc_source'] in ("District column", "Address text"):
                    sheet_loc["n_text"] += 1
                else:
                    sheet_loc["n_un"] += 1
            self.sheet_report.append((sn, status, type_hint or "(auto)", n_rows,
                                      "ok" if n_rows else "headers found, no data rows yet — not yet published"))

        GIS.save_loc_cache()    # persist locate results → re-syncs are instant

        # CRITICAL on Windows: read_only mode keeps the .xlsx file handle
        # open. Without close(), the cached cloud workbook stays LOCKED by
        # this very app, and the next Google-Sheet sync's os.replace() fails
        # with [WinError 5] Access is denied.
        wb.close()

        # De-duplicate (duplicate pivot-export sheets carry identical rows)
        seen = set()
        deduped = []
        for r in raw:
            key = (r['type'], r['status'], r['project'], r['capacity_mw'], r['lic_or_app_no'])
            if key in seen: continue
            seen.add(key)
            deduped.append(r)
        self.records = deduped

    # ── query helpers ──────────────────────────────────────────────────────
    def get_types(self):
        return sorted({r['type'] for r in self.records}, key=lambda t: TYPE_ORDER.index(t) if t in TYPE_ORDER else 99)

    def get_statuses(self):
        """Pipeline-funnel stages only (Application→...→Operating). The
        side categories (Cancelled / GoN Study Project / Technical
        Clearance) are deliberately excluded here — see get_extra_statuses()
        — so they don't show up as odd extra checkboxes in the existing
        Power Plants / Map / Transmission tabs' "License stage" filters."""
        return sorted({r['status'] for r in self.records if r['status'] in STATUS_ORDER},
                      key=lambda s: STATUS_ORDER.index(s))

    def get_extra_statuses(self):
        """The side categories actually present in the loaded workbook,
        in a fixed display order."""
        present = {r['status'] for r in self.records if r['status'] in EXTRA_STATUS_COLORS}
        return [s for s in EXTRA_STATUS_ORDER if s in present]

    def get_substages(self, statuses=None):
        """Distinct substage labels present (optionally restricted to a set
        of parent statuses), e.g. the specific cancellation stage or
        GoN Studied/Under Study split."""
        recs = self.records if not statuses else [r for r in self.records if r['status'] in statuses]
        return sorted({r['substage'] for r in recs if r.get('substage')})

    def get_voltages(self):
        return sorted({r['voltage_kv'] for r in self.records if r['voltage_kv']})

    def get_provinces(self):
        """Always returns all 7 provinces (in the standard order), plus
        "Unspecified" if any record actually falls into it. Provinces are
        listed even when the loaded workbook has zero matching rows for them
        (e.g. Madhesh often has little/no hydropower, since it's flat Terai
        rather than hill/mountain terrain) — so the filter option is never
        silently missing just because today's data happens to be thin there."""
        present = {r['province'] for r in self.records}
        ordered = list(PROVINCE_ORDER)
        if "Unspecified" in present:
            ordered.append("Unspecified")
        return ordered

    def get_districts(self):
        present = sorted({r['district'] for r in self.records if r['district']})
        if any(r['district'] is None for r in self.records):
            present = present + ["Unspecified"]
        return present

    def get_unmapped_districts(self):
        """Raw district/VDC text that could not be resolved to any of the 7
        provinces — these rows show up as province='Unspecified'. Surfaced on
        the Overview tab so exact wording can be added to DISTRICT_ALIASES."""
        return sorted(self.unmapped_districts.items(), key=lambda kv: -kv[1])

    def get_bs_year_bounds(self):
        years = [r['primary_date'][0] for r in self.records if r['primary_date']]
        if not years: return (None, None)
        return (min(years), max(years))

    def get_license_year_bounds(self):
        years = [license_year(r) for r in self.records if license_year(r) is not None]
        if not years: return (None, None)
        return (min(years), max(years))

    def filter(self, types=None, statuses=None, cap_min=None, cap_max=None,
               year_from=None, year_to=None, voltages=None, search=None,
               provinces=None, districts=None, cod_from=None, cod_to=None,
               substages=None, locals_sel=None):
        out = []
        for r in self.records:
            if types and r['type'] not in types: continue
            if statuses and r['status'] not in statuses: continue
            if locals_sel and (record_local(r) or "") not in locals_sel: continue
            if substages and r.get('substage') not in substages: continue
            if cap_min is not None and (r['capacity_mw'] is None or r['capacity_mw'] < cap_min): continue
            if cap_max is not None and (r['capacity_mw'] is None or r['capacity_mw'] >= cap_max): continue
            if voltages and r['voltage_kv'] not in voltages: continue
            if provinces and r['province'] not in provinces: continue
            if districts and (r['district'] or "Unspecified") not in districts: continue
            if (year_from is not None or year_to is not None):
                # Full B.S. date filtering: bounds may be (y, m, d) tuples
                # (from the YYYY[-MM[-DD]] filter boxes) or bare years.
                t = r['primary_date']
                if t is None: continue
                o = bs_ordinal(t)
                if year_from is not None:
                    lo = bs_ordinal(year_from if isinstance(year_from, tuple)
                                    else (int(year_from), 1, 1))
                    if o < lo: continue
                if year_to is not None:
                    hi = bs_ordinal(year_to if isinstance(year_to, tuple)
                                    else (int(year_to), 12, 32))
                    if o > hi: continue
            if (cod_from is not None or cod_to is not None):
                # COD (Commercial Operation Date) filter — meaningful mainly
                # for "Operating" plants, since only those carry a COD.
                t = r.get('cod_bs')
                if t is None: continue
                o = bs_ordinal(t)
                if cod_from is not None:
                    lo = bs_ordinal(cod_from if isinstance(cod_from, tuple)
                                    else (int(cod_from), 1, 1))
                    if o < lo: continue
                if cod_to is not None:
                    hi = bs_ordinal(cod_to if isinstance(cod_to, tuple)
                                    else (int(cod_to), 12, 32))
                    if o > hi: continue
            if search:
                s = search.lower()
                hay = f"{r['project']} {r['promoter'] or ''} {r['district'] or ''}".lower()
                if s not in hay: continue
            out.append(r)
        return out

    def kpi_matrix(self, records=None):
        records = self.records if records is None else records
        kpi = defaultdict(lambda: [0, 0.0])
        for r in records:
            k = (r['type'], r['status'])
            kpi[k][0] += 1
            kpi[k][1] += r['capacity_mw'] or 0.0
        return kpi

    def district_metric(self, records):
        """{official district name: [project count, capacity MW]} — used by
        the GIS Map choropleth. Only rows whose district resolves to one of
        the 77 official polygons are counted (others appear in the side note)."""
        out = defaultdict(lambda: [0, 0.0])
        official = set(GIS.district_province) if GIS.loaded else None
        for r in records:
            d = r['district']
            if not d or (official and d not in official):
                continue
            out[d][0] += 1
            out[d][1] += r['capacity_mw'] or 0.0
        return out

    def gis_location_stats(self, records=None):
        records = self.records if records is None else records
        n_gis = sum(1 for r in records if r['loc_source'] == "GIS coordinates")
        n_txt = sum(1 for r in records if r['loc_source'] in ("District column", "Address text"))
        n_un  = len(records) - n_gis - n_txt
        return n_gis, n_txt, n_un

    def yearly_series(self, records, key_field="type"):
        """{year: {key: [count, capacity_mw]}} keyed by License Issued Year (B.S.)."""
        out = defaultdict(lambda: defaultdict(lambda: [0, 0.0]))
        for r in records:
            y = license_year(r)
            if y is None: continue
            k = r.get(key_field) or "Unspecified"
            out[y][k][0] += 1
            out[y][k][1] += r['capacity_mw'] or 0.0
        return out


