#  GIS LOCATION ENGINE  (hermes_NPL_new_wgs shapefile package)
#
#  Locates every plant inside the OFFICIAL district polygons by
#  point-in-polygon on its map coordinates, instead of trusting free-text
#  district cells. A feature whose geometry touches several districts
#  (e.g. a transmission line with from/to coordinates, or a hydro project
#  whose river forms a district border) is assigned to the district that
#  contains the MAJORITY of its geometry (the polyline is densified and each
#  sample point votes for the district it falls in).
#
#  The district -> province relation used by the 7-province filter is taken
#  straight from the PROVINCE attribute of the GIS layer — the authoritative
#  2015-constitution map — and overrides the hand-typed table above.
# ─────────────────────────────────────────────────────────────────────────────
