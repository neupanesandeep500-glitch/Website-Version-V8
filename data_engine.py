    def _find_pa_reader(self, extra_dirs=None):
        import glob, zipfile, io as _io
        dirs = list(extra_dirs or []) + _app_base_dirs()
        # loose shapefiles (also one folder deep)
        for base in dirs:
            for pat in (os.path.join(base, "*.shp"), os.path.join(base, "*", "*.shp")):
                for shp in glob.glob(pat):
                    bn = os.path.basename(shp).lower()
                    if "protect" in bn or "संरक्षित" in os.path.basename(shp):
                        try:
                            return _pyshp.Reader(shp[:-4], encoding="utf-8"), shp
                        except Exception:
                            continue
        # zips
        for base in dirs:
            for zpath in sorted(glob.glob(os.path.join(base, "*.zip"))):
                bn = os.path.basename(zpath).lower()
                if "protect" not in bn:
                    continue
                try:
                    zf = zipfile.ZipFile(zpath)
                except Exception:
                    continue
                shps = [n for n in zf.namelist() if n.lower().endswith(".shp")]
                if not shps:
                    continue
                stem = shps[0][:-4]
                def part(ext):
                    try: return _io.BytesIO(zf.read(stem + ext))
                    except KeyError: return None
                return _pyshp.Reader(shp=_io.BytesIO(zf.read(shps[0])),
                                     shx=part(".shx"), dbf=part(".dbf"),
                                     encoding="utf-8"), zpath
        return None

