# ─────────────────────────────────────────────────────────────────────────────
#  COORDINATE PARSING  (decimal degrees or DMS, from the DoED workbook)
# ─────────────────────────────────────────────────────────────────────────────
# DoED tables write DMS with a letter-o degree mark: 27o42'18"
_DMS_RE = re.compile(
    r"""^\s*(-?\d+(?:\.\d+)?)\s*(?:[°ºoOd:\s]\s*(\d+(?:\.\d+)?))?
        \s*(?:['′m:\s]\s*(\d+(?:\.\d+)?))?\s*["″s]?\s*([NSEWnsew])?\s*$""",
    re.VERBOSE)

def parse_coord(value):
    """Parse one latitude/longitude cell. Accepts 27.7052, '27.7052',
    '27°42'18\"N', '27 42 18', '27:42:18' ... Returns float degrees or None."""
    if value is None:
        return None
    if isinstance(value, (int, float)):
        return float(value)
    s = str(value).strip().replace(",", ".")
    if not s:
        return None
    try:
        return float(s)
    except ValueError:
        pass
    m = _DMS_RE.match(str(value).strip())
    if not m:
        return None
    deg = float(m.group(1))
    mins = float(m.group(2) or 0)
    sec = float(m.group(3) or 0)
    v = abs(deg) + mins / 60.0 + sec / 3600.0
    hemi = (m.group(4) or "").upper()
    if deg < 0 or hemi in ("S", "W"):
        v = -v
    return v

def valid_nepal_pair(lat, lon):
    """Sanity-check a (lat, lon) pair against Nepal's extent; silently swaps
    the two if they were entered the wrong way round. Returns (lat, lon) or
    (None, None)."""
    if lat is None or lon is None:
        return None, None
    if NEPAL_LAT[0] <= lat <= NEPAL_LAT[1] and NEPAL_LON[0] <= lon <= NEPAL_LON[1]:
        return lat, lon
    if NEPAL_LAT[0] <= lon <= NEPAL_LAT[1] and NEPAL_LON[0] <= lat <= NEPAL_LON[1]:
        return lon, lat          # columns were swapped
    return None, None

def _expand_merged_cols(headers, i, max_extra=3):
    """DoED sheets write 'Latitiude N' / 'Longitude E' as a MERGED header
    spanning the min and max columns — openpyxl reports the text only in the
    first cell and None in the continuation cell(s). Return [i, i+1, ...]
    including those blank-headed continuation columns, so the min/max pair
    is captured and the licence AREA (bounding box) can be reconstructed."""
    idxs = [i]
    j = i + 1
    while (j < len(headers) and (j - i) <= max_extra
           and (headers[j] is None or str(headers[j]).strip() == "")):
        idxs.append(j)
        j += 1
    return idxs

def find_latlon_cols(headers):
    """Raw latitude / longitude column index lists, in order of appearance.
    Merged (blank-continuation) columns are included, which is what turns a
    'Latitiude N | · | Longitude E | ·' layout into the 2×2 min/max set that
    drives licence-area bounding boxes."""
    lat_idx, lon_idx = [], []
    for i, h in enumerate(headers):
        hn = norm_header(h)
        if not hn:
            continue
        if re.search(r"\blat", hn):
            lat_idx.extend(_expand_merged_cols(headers, i))
        elif re.search(r"\b(lon|lng|long)", hn):
            lon_idx.extend(_expand_merged_cols(headers, i))
    return lat_idx, lon_idx

def find_latlon_col_pairs(headers):
    """Pair up latitude / longitude columns in order of appearance — handles
    single Lat/Long pairs as well as From/To pairs on transmission sheets.
    Merged (blank-continuation) min/max columns are included via
    find_latlon_cols, so corner pairs still line up."""
    lat_idx, lon_idx = find_latlon_cols(headers)
    return list(zip(lat_idx, lon_idx))

# ── UTM support ──────────────────────────────────────────────────────────────
# DoED sheets frequently record plant locations as UTM Easting / Northing
# (WGS-84, zones 44 & 45 across Nepal) rather than latitude / longitude.
NEPAL_UTM_E = (150000.0, 900000.0)
NEPAL_UTM_N = (2880000.0, 3450000.0)

def utm_to_latlon(easting, northing, zone):
    """Inverse Transverse Mercator (WGS-84). Returns (lat, lon) degrees."""
    a = 6378137.0
    f = 1 / 298.257223563
    k0 = 0.9996
    e2 = f * (2 - f)
    ep2 = e2 / (1 - e2)
    x = easting - 500000.0
    m = northing / k0
    mu = m / (a * (1 - e2/4 - 3*e2**2/64 - 5*e2**3/256))
    se = math.sqrt(1 - e2)
    e1 = (1 - se) / (1 + se)
    fp = (mu + (3*e1/2 - 27*e1**3/32) * math.sin(2*mu)
             + (21*e1**2/16 - 55*e1**4/32) * math.sin(4*mu)
             + (151*e1**3/96) * math.sin(6*mu)
             + (1097*e1**4/512) * math.sin(8*mu))
    sf, cf, tf = math.sin(fp), math.cos(fp), math.tan(fp)
    c1 = ep2 * cf * cf
    t1 = tf * tf
    n1 = a / math.sqrt(1 - e2 * sf * sf)
    r1 = a * (1 - e2) / (1 - e2 * sf * sf) ** 1.5
    d = x / (n1 * k0)
    lat = fp - (n1 * tf / r1) * (d*d/2
          - (5 + 3*t1 + 10*c1 - 4*c1*c1 - 9*ep2) * d**4 / 24
          + (61 + 90*t1 + 298*c1 + 45*t1*t1 - 252*ep2 - 3*c1*c1) * d**6 / 720)
    lon = math.radians((zone - 1) * 6 - 180 + 3) + (
          d - (1 + 2*t1 + c1) * d**3 / 6
            + (5 - 2*c1 + 28*t1 - 3*c1*c1 + 8*ep2 + 24*t1*t1) * d**5 / 120) / cf
    return math.degrees(lat), math.degrees(lon)

def utm_pair_to_latlon(e, n):
    """Convert an (easting, northing) pair, auto-detecting UTM zone 44 vs 45
    by whichever lands inside Nepal. Returns (lat, lon) or (None, None)."""
    if e is None or n is None:
        return None, None
    # tolerate swapped columns
    if not (NEPAL_UTM_E[0] <= e <= NEPAL_UTM_E[1] and NEPAL_UTM_N[0] <= n <= NEPAL_UTM_N[1]):
        if NEPAL_UTM_E[0] <= n <= NEPAL_UTM_E[1] and NEPAL_UTM_N[0] <= e <= NEPAL_UTM_N[1]:
            e, n = n, e
        else:
            return None, None
    # Nepal straddles UTM zones 44 & 45; a wrong-zone conversion can still
    # land inside the country's bounding BOX, so candidates are checked
    # against the actual district POLYGONS whenever GIS is loaded.
    fallback = None
    for zone in (44, 45):
        la, lo = utm_to_latlon(e, n, zone)
        la2, lo2 = valid_nepal_pair(la, lo)
        if la2 is None:
            continue
        if GIS.loaded:
            d, _ = GIS.locate(la2, lo2)
            if d:
                return la2, lo2
            if fallback is None:
                fallback = (la2, lo2)
        else:
            return la2, lo2
    if fallback:
        return fallback
    return None, None

def find_utm_col_pairs(headers):
    """Pair Easting / Northing (or UTM X / UTM Y) columns in order."""
    e_idx, n_idx = [], []
    for i, h in enumerate(headers):
        hn = norm_header(h)
        if not hn:
            continue
        if re.search(r"\beast", hn) or ("utm" in hn and re.search(r"\bx\b", hn)):
            e_idx.append(i)
        elif re.search(r"\bnorth", hn) or ("utm" in hn and re.search(r"\by\b", hn)):
            n_idx.append(i)
    return list(zip(e_idx, n_idx))

def find_dms_triplet(headers, axis):
    """DoED sheets sometimes split coordinates into separate Degree / Minute /
    Second columns ('Latitude (Deg)', 'Latitude (Min)', ...). Returns
    (deg_idx, min_idx_or_None, sec_idx_or_None) or None."""
    pat = r"\blat" if axis == "lat" else r"\b(lon|lng|long)"
    # If this axis has a Max column, then Min/Max are RANGE bounds of the
    # licence area (DoED style), not degree-minute-second parts.
    for h in headers:
        hn = norm_header(h)
        if hn and re.search(pat, hn) and re.search(r"\bmax", hn):
            return None
    deg = mnt = sec = plain = None
    for i, h in enumerate(headers):
        hn = norm_header(h)
        if not hn or not re.search(pat, hn):
            continue
        if "deg" in hn or "°" in str(h):
            deg = i if deg is None else deg
        elif re.search(r"\bmin", hn):
            mnt = i if mnt is None else mnt
        elif re.search(r"\bsec", hn):
            sec = i if sec is None else sec
        else:
            plain = i if plain is None else plain
    if deg is None and (mnt is not None or sec is not None):
        deg = plain          # 'Latitude' + 'Lat (Min)' + 'Lat (Sec)' style
    if deg is not None and (mnt is not None or sec is not None):
        return (deg, mnt, sec)
    return None

def combine_dms(row, triplet):
    deg_i, min_i, sec_i = triplet
    d = to_float(row[deg_i]) if deg_i is not None and deg_i < len(row) else None
    if d is None:
        return None
    m = to_float(row[min_i]) if min_i is not None and min_i < len(row) else 0.0
    s = to_float(row[sec_i]) if sec_i is not None and sec_i < len(row) else 0.0
    v = abs(d) + (m or 0.0) / 60.0 + (s or 0.0) / 3600.0
    return -v if d < 0 else v


_FLOAT_RE = re.compile(r"-?\d{1,3}\.\d+")

def coords_from_text(cell):
    """Pull (lat, lon) pairs out of a free-form coordinates cell like
    '27.70, 85.32' or 'From 27.70/85.32 to 27.95/85.10'."""
    if not cell:
        return []
    nums = [float(x) for x in _FLOAT_RE.findall(str(cell))]
    pairs = []
    for a, b in zip(nums[0::2], nums[1::2]):
        la, lo = valid_nepal_pair(a, b)
        if la is not None:
            pairs.append((la, lo))
    return pairs


