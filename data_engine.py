def sheet_id_from_url(url_or_id):
    """Accepts a full Google-Sheets URL or a bare spreadsheet ID."""
    if not url_or_id:
        return None
    m = re.search(r"/spreadsheets/d/([a-zA-Z0-9\-_]+)", url_or_id)
    if m:
        return m.group(1)
    if re.fullmatch(r"[a-zA-Z0-9\-_]{20,}", url_or_id.strip()):
        return url_or_id.strip()
    return None

def download_google_sheet_xlsx(url_or_id, dest_path, timeout=60):
    """Download the whole multi-tab Google Sheet as an .xlsx workbook using
    the official export endpoint. Requires the sheet to be shared as
    'Anyone with the link: Viewer' (no password — Google does not permit
    plain e-mail/password sign-in from applications). Returns dest_path."""
    sid = sheet_id_from_url(url_or_id)
    if not sid:
        raise RuntimeError("Could not read a spreadsheet ID from the Google Sheet URL. "
                           "Paste the full https://docs.google.com/spreadsheets/d/… link.")
    export = f"https://docs.google.com/spreadsheets/d/{sid}/export?format=xlsx"
    req = urllib.request.Request(export, headers={"User-Agent": "Mozilla/5.0 (PowerPlantDashboard v4.0)"})
    with urllib.request.urlopen(req, timeout=timeout) as resp:
        data = resp.read()
    # Short-circuit: if the downloaded bytes are identical to the cached
    # copy, there is nothing to reload — the caller can skip the whole
    # parse + re-render cycle (this is what makes the periodic auto-sync
    # invisible instead of freezing the app every N minutes).
    if data.startswith(b"PK") and os.path.exists(dest_path):
        try:
            with open(dest_path, "rb") as fh:
                if hashlib.sha256(fh.read()).digest() == hashlib.sha256(data).digest():
                    return dest_path, False
        except Exception:
            pass
    # A real .xlsx is a ZIP container and starts with the bytes "PK".
    # HTML here means Google served a sign-in page → sharing is off.
    if not data.startswith(b"PK"):
        raise RuntimeError(
            "Google returned a sign-in page instead of the workbook.\n\n"
            "Open the Google Sheet → Share → General access →\n"
            "'Anyone with the link' (Viewer), then sync again.\n\n"
            "(Google blocks e-mail/password logins from apps by design —\n"
            "link-sharing or a service account are the supported options.)")
    fd, tmp = tempfile.mkstemp(suffix=".xlsx", dir=os.path.dirname(dest_path) or ".")
    with os.fdopen(fd, "wb") as fh:
        fh.write(data)
    # Atomic swap. On Windows, replace fails with [WinError 5] if any other
    # process (an old app instance, Excel, antivirus) still holds the cached
    # file open — retry briefly, then fall back to a side-by-side filename so
    # the fresh data is never lost.
    for attempt in range(4):
        try:
            os.replace(tmp, dest_path)
            return dest_path, True
        except PermissionError:
            if attempt == 3:
                alt = os.path.join(
                    os.path.dirname(dest_path),
                    f"cloud_workbook_{datetime.datetime.now():%Y%m%d_%H%M%S}.xlsx")
                os.replace(tmp, alt)
                return alt, True
            time.sleep(0.6)
    return dest_path, True

_DRIVE_ID_RE = re.compile(r"/file/d/([a-zA-Z0-9_-]{20,})|[?&]id=([a-zA-Z0-9_-]{20,})")

def drive_id_from_url(url_or_id):
    """Accepts a full Google Drive share URL or a bare file ID."""
    if not url_or_id:
        return None
    m = _DRIVE_ID_RE.search(url_or_id)
    if m:
        return m.group(1) or m.group(2)
    if re.fullmatch(r"[a-zA-Z0-9_-]{20,}", url_or_id.strip()):
        return url_or_id.strip()
    return None


def download_google_drive_file(url_or_id, dest_path, timeout=90):
    """Download a file shared as 'Anyone with the link: Viewer' straight
    from Google Drive — used for the GIS shapefile package and the
    protected-area package so both can be kept in Drive (fast to update,
    no redeploy needed) instead of committed to the repo. Handles Drive's
    'file too large to scan for viruses' interstitial for zips over ~25MB
    by re-requesting with the confirm token, which is the only extra step
    needed for a public, non-authenticated download.
    Returns (dest_path, changed: bool)."""
    fid = drive_id_from_url(url_or_id)
    if not fid:
        raise RuntimeError(
            "Could not read a file ID from that Google Drive link. Paste the "
            "full https://drive.google.com/file/d/…/view link (share as "
            "'Anyone with the link').")

    base = "https://drive.google.com/uc?export=download"
    session_cookie = None

    def _fetch(url):
        req = urllib.request.Request(
            url, headers={"User-Agent": "Mozilla/5.0 (PowerPlantDashboard v4.0)"})
        if session_cookie:
            req.add_header("Cookie", session_cookie)
        return urllib.request.urlopen(req, timeout=timeout)

    resp = _fetch(f"{base}&id={fid}")
    data = resp.read()
    ctype = resp.headers.get("Content-Type", "")

    # Large files: Drive serves an HTML "can't scan for viruses" page with a
    # confirm token embedded in it rather than the file bytes directly.
    if "text/html" in ctype:
        html = data.decode("utf-8", errors="ignore")
        m = re.search(r"confirm=([0-9A-Za-z_-]+)", html)
        cookie_hdr = resp.headers.get("Set-Cookie", "")
        if m:
            token = m.group(1)
            session_cookie = cookie_hdr
            resp = _fetch(f"{base}&id={fid}&confirm={token}")
            data = resp.read()
            ctype = resp.headers.get("Content-Type", "")

    if "text/html" in ctype or not data.startswith(b"PK"):
        raise RuntimeError(
            "Google Drive returned a web page instead of the file — the "
            "link is probably not shared as 'Anyone with the link: Viewer'. "
            "Open the file in Drive -> Share -> General access -> change it, "
            "then sync again.")

    if os.path.exists(dest_path):
        try:
            with open(dest_path, "rb") as fh:
                if hashlib.sha256(fh.read()).digest() == hashlib.sha256(data).digest():
                    return dest_path, False
        except Exception:
            pass

    fd, tmp = tempfile.mkstemp(suffix=".zip", dir=os.path.dirname(dest_path) or ".")
    with os.fdopen(fd, "wb") as fh:
        fh.write(data)
    os.replace(tmp, dest_path)
    return dest_path, True


